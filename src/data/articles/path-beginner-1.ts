- 日常使用的自然度一般
**适合人群：** 学术用户、程序员，或者想本地部署开源模型的用户。
国内工具小结：新手直接选豆包当主力，开箱即用，中文好，功能够，免费就够用；需要处理长PDF和大文档，搭配个Kimi；需要处理表格和办公，用通义千问。这三个搭配起来，99%的国内用户需求都能满足，一个月花30块钱都不到。

