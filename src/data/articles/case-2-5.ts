SD/ComfyUI的源文件更完整，一定要保存好：
1. **保存工作流**：ComfyUI默认生成的PNG图片里就内嵌了完整的工作流（包括所有参数、用的模型/LoRA路径），把最终版图片拖回ComfyUI就能一键恢复所有节点。不要删除这些PNG，它们本身就是源文件
