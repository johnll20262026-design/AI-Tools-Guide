3. **真诚是最大的技巧**：不要装大神、不要吹牛逼、不要抄别人内容、不要标题党，真诚一点，写你真正做过的东西，错了就承认，不懂就说不懂，长期下来大家会信任你。靠博眼球、标题党赚来的流量来得快去得也快，靠真实能力积累的信任是长期的。
